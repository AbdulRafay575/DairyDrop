const mongoose = require('mongoose');
const bcrypt = require('bcryptjs');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: [true, 'Name is required'],
    trim: true,
    maxlength: [50, 'Name cannot be more than 50 characters']
  },
  email: {
    type: String,
    required: [true, 'Email is required'],
    unique: true,
    lowercase: true,
    match: [/^\w+([.-]?\w+)*@\w+([.-]?\w+)*(\.\w{2,3})+$/, 'Please enter a valid email']
  },
  phone: {
    type: String,
    required: [true, 'Phone number is required'],
    match: [/^\+?[\d\s-()]{10,}$/, 'Please enter a valid phone number']
  },
  password: {
    type: String,
    required: [true, 'Password is required'],
    minlength: [6, 'Password must be at least 6 characters'],
    select: false
  },
  role: {
    type: String,
    enum: ['user', 'admin'],
    default: 'user'
  },
  addresses: [{
    type: {
      type: String,
      enum: ['home', 'work', 'other'],
      default: 'home'
    },
    street: String,
    city: String,
    state: String,
    zipCode: String,
    country: {
      type: String,
      default: 'India'
    },
    isDefault: {
      type: Boolean,
      default: false
    }
  }],
  isActive: {
    type: Boolean,
    default: true
  }
}, {
  timestamps: true
});

// Add pepper to password before hashing
userSchema.pre('save', async function () {
  if (!this.isModified('password')) return; // no next()
  
  // Add pepper to password
  const pepperedPassword = this.password + process.env.PEPPER;
  
  // Hash password with salt rounds
  this.password = await bcrypt.hash(pepperedPassword, parseInt(process.env.BCRYPT_SALT_ROUNDS));
});


// Compare password method
userSchema.methods.comparePassword = async function(candidatePassword) {
  const pepperedPassword = candidatePassword + process.env.PEPPER;
  return await bcrypt.compare(pepperedPassword, this.password);
};

module.exports = mongoose.model('User', userSchema);